#include "3-calc.h"

/**
 * op_add - does a + b
 *
 * @a: first number
 * @b: second number
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - does a - b
 *
 * @a: first number
 * @b: second number
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - does a * b
 *
 * @a: first number
 * @b: second number
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - does a / b
 *
 * @a: first number
 * @b: second number
 *
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - does a % b
 *
 * @a: first number
 * @b: second number
 *
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
